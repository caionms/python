print('Iremos calcular a redução do tempo de vida de um fumante.')
qtd_cigarros = int(input('Digite a quantidade de cigarros fumados por dia: '))
qtd_anos = int(input('Digite a quantidade de anos que ele fumou: '))
print(f'Esse fumante perdeu {((qtd_cigarros*10)*(qtd_anos*365))} dias de sua vida.')
